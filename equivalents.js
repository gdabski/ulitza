import * as R from "ramda"

export const equivalentStreet = (country) => (street) =>
  R.pipe(
    R.find(R.find(R.equals(street))),
    // Return the street name as is, if no equivalent found.
    R.defaultTo([street]),
    R.head,    
  )(equivalents[country])

const duplicates = (country) =>
  R.pipe(
    R.flatten,
    R.groupBy(R.identity),
    R.mapObjIndexed(R.length),
    R.toPairs,
    R.reject(R.compose(R.equals(1), R.prop(1))),    
  )(equivalents[country])

const allDuplicates = () =>
  R.pipe(
    R.map(country => [country, duplicates(country)]),    
    R.reject(R.compose(R.equals([]), R.prop(1)))
  )(R.keys(equivalents))


